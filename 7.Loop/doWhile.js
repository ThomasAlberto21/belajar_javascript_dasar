// Todo Do While
/*

    * Looping atau pengulangan dengan do ... while digunakan untuk melakukan pengulangan suatu kelompok perintah berdasarkan kondisi tertentu, dengan pemeriksaan kondisi dilakukan setelah pengulangan dilakukan satu kali. Pada pengulangan dengan do .. while proses pemeriksaan kondisi dilakukan setelah blok perintah pengulangan dikerjakan terlebih dahulu, dan pengulangan akan dilakukan jika kondisi masih bernilai benar (true).

*/


// Contoh
let i = 1;

do {
  console.log(i);
  i++;
} while (i <= 100);